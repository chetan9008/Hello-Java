import java.lang.*;

public class typecasting {
    public static void main(String[] args) {
        long variable = 2;
        System.out.println(variable);

        float x = (float) 3 / 2;
        System.out.println(x);

        System.out.println();

        double y = Math.cos(2.2);
        y = Math.abs(2.2);
        y = Math.sqrt(2.2);
        y = Math.floor(2.9);
        System.out.println(y);
    }

}
